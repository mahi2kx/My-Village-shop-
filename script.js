function renderAll() {
    renderProducts("wines-grid", products.wines);
    renderProducts("cool-drinks-grid", products.coolDrinks);
    renderProducts("chicken-grid", products.chicken);

    // logout button control
    let btn = document.getElementById("logout-btn");
    if (btn) btn.style.display = isAdmin ? "inline-block" : "none";
}

function renderProducts(id, list) {
    const container = document.getElementById(id);
    container.innerHTML = "";

    list.forEach(p => {
        container.innerHTML += `
        <div class="product-card">

            <div class="product-image">
                <img src="${p.image}" alt="${p.name}">
            </div>

            <h3>${p.name}</h3>
            <div class="product-price">₹${p.price}</div>
            <p class="stock">
    ${p.inStock === false ? "Out of Stock ❌" : "In Stock ✅"}
</p>
            <button onclick="addToCart('${p.id}')">Add to Cart</button>

            ${isAdmin ? `
                <div class="admin-buttons">
                    <button onclick="editPrice('${p.id}')">Edit Price</button>
                    <button onclick="toggleStock('${p.id}')">Stock</button>
                </div>
            ` : ``}

        </div>
        `;
    });
}

